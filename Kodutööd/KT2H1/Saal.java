package Kodutööd.KT2H1;

public class Saal {
    int number;
    int kohtadeArv;

    public Saal(int number, int kohtadeArv) {
        this.number = number;
        this.kohtadeArv = kohtadeArv;
    }

    public int getNumber() {
        return number;
    }

    public int getKohtadeArv() {
        return kohtadeArv;
    }
}
